package com.curso.java.oo.model;

public class Ordenadores {
	private Integer numeroDeOrdenador;
	private String tipoOrdenador;
	private String marcaOrdenador;
	
	public Integer getNumeroDeOrdenador() {
		return numeroDeOrdenador;
	}
	public void setNumeroDeOrdenador(Integer numeroDeOrdenador) {
		this.numeroDeOrdenador = numeroDeOrdenador;
	}
	public String getTipoOrdenador() {
		return tipoOrdenador;
	}
	public void setTipoOrdenador(String tipoOrdenador) {
		this.tipoOrdenador = tipoOrdenador;
	}
	public String getMarcaOrdenador() {
		return marcaOrdenador;
	}
	public void setMarcaOrdenador(String marcaOrdenador) {
		this.marcaOrdenador = marcaOrdenador;
	}
	
	
	
}
